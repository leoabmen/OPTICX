    private void chkCombinadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chkCombinadoActionPerformed
        try {
            if (chkCombinado.isSelected()){
                gbTipoEnfermedad.remove(rbMiopia);
                gbTipoEnfermedad.remove(rbAstigmatismo);
                gbTipoEnfermedad.remove(rbHipermetropia);
            } else{
                gbTipoEnfermedad.add(rbMiopia);
                gbTipoEnfermedad.add(rbAstigmatismo);
                gbTipoEnfermedad.add(rbHipermetropia);
                gbTipoEnfermedad.clearSelection();
                cboMiopia.setEnabled(false);
                cboMiopia.setSelectedIndex(0);
                cboAstigmatismo.setEnabled(false);
                cboAstigmatismo.setSelectedIndex(0);
                cboHipermetropia.setEnabled(false);
                cboHipermetropia.setSelectedIndex(0);
            }

        } catch (Exception e) {
        }
    }//GEN-LAST:event_chkCombinadoActionPerformed

