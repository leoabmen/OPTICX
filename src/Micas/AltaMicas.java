    private void cbxTipoLenteItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbxTipoLenteItemStateChanged
        if (cbxTipoLente.getSelectedItem().equals("Monofocal")){
            pnlBifocal.setVisible(false);
            cbxTipoBifocal.removeAllItems();
            pnlTiposGenerales.setVisible(true);
            pnlInfoAdicional.setVisible(true);
            btnGuardar.setEnabled(true);
            txtClave.requestFocus();
            CargarCombosTipos();
        }else if (cbxTipoLente.getSelectedItem().equals("Bifocal")){

            try {
                Object[] llenar_combo = con.combox("tipo_de_bifocal", "TIPO_BIFO_NOMB");
                cbxTipoBifocal.removeAllItems();
                cbxTipoBifocal.addItem("SELECCIONA:");
                for (int i = 0; i < llenar_combo.length; i++) {

                    cbxTipoBifocal.addItem((String) llenar_combo[i]);
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "¡Error! no se ha podido acceder ala base de datos para cargar la lista de Tipo de Bifocal");
            }
            pnlBifocal.setVisible(true);
            pnlTiposGenerales.setVisible(false);
            pnlInfoAdicional.setVisible(false);
            btnGuardar.setEnabled(false);
        }
        else if (cbxTipoLente.getSelectedItem().equals("SELECCIONA:")){
            pnlBifocal.setVisible(false);
            pnlTiposGenerales.setVisible(false);
            pnlInfoAdicional.setVisible(false);
            btnGuardar.setEnabled(false);
            cbxTipoBifocal.removeAllItems();
        }
    }//GEN-LAST:event_cbxTipoLenteItemStateChanged

